/**
 * TaskCommands - Паттерны для распознавания команд работы с задачами
 */

export const taskPatterns = {
  en: {
    add: [
      // Прямые команды добавления
      /(?:add|create|new)\s+(?:task\s+)?(.+)/i,
      /(?:add|create|new)\s+(.+?)\s+(?:task|todo)/i,

      // Естественные формулировки
      /i\s+need\s+to\s+(.+)/i,
      /i\s+have\s+to\s+(.+)/i,
      /remind\s+me\s+to\s+(.+)/i,
      /don't\s+forget\s+to\s+(.+)/i,

      // Вопросительные формы
      /can\s+(?:you\s+)?(?:add|create)\s+(?:task\s+)?(.+)/i,
      /could\s+(?:you\s+)?(?:add|create)\s+(?:task\s+)?(.+)/i,

      // Fallback: любой текст, похожий на задачу
      /^(?!.*\b(?:show|go|navigate|switch|delete|complete|mark|finish)\b)(.+)$/i
    ],
    toggle: [
      // Прямые команды завершения
      /(?:complete|finish|mark|done|tick)\s+(?:task\s+)?(\d+)/i,
      /(?:task\s+)?(\d+)\s+(?:complete|finish|done)/i,

      // По названию задачи
      /(?:complete|finish|mark)\s+(?:task\s+)?["']?(.+?)["']?$/i,
      /(?:mark\s+)?["']?(.+?)["']?\s+(?:as\s+)?(?:complete|done)/i,

      // Естественные формулировки
      /i\s+(?:did|completed|finished)\s+(.+)/i,
      /done\s+with\s+(.+)/i
    ],
    delete: [
      // Прямые команды удаления
      /(?:delete|remove|trash)\s+(?:task\s+)?(\d+)/i,
      /(?:task\s+)?(\d+)\s+(?:delete|remove)/i,

      // Естественные формулировки
      /get\s+rid\s+of\s+(?:task\s+)?(\d+)/i,
      /remove\s+(?:task\s+)?(\d+)/i
    ]
  },
  ru: {
    add: [
      // Прямые команды добавления
      /(?:добавь|создай|новая)\s+(?:задачу\s+)?(.+)/i,
      /(?:добавь|создай)\s+(.+?)\s+(?:задачу|задачу)/i,

      // Естественные формулировки
      /мне\s+надо\s+(.+)/i,
      /мне\s+нужно\s+(.+)/i,
      /напомни\s+мне\s+(.+)/i,
      /не\s+забудь\s+(.+)/i,

      // Вопросительные формы
      /(?:можешь|можешь)\s+(?:добавить|создать)\s+(?:задачу\s+)?(.+)/i,
      /(?:добавь|создай)\s+(?:задачу\s+)?(.+)/i,

      // Fallback: любой текст, похожий на задачу
      /^(?!.*\b(?:покажи|перейди|переключи|удали|заверши|отметь|выполни)\b)(.+)$/i
    ],
    toggle: [
      // Прямые команды завершения (с числом)
      /(?:заверши|отметь|выполни|сделай)\s+(?:задачу\s+)?(\d+)/i,
      /(?:задачу\s+)?(\d+)\s+(?:заверши|отметь|выполни)/i,

      // Завершение по текстовому числу (один, два, три...)
      /(?:заверши|отметь|выполни|сделай)\s+(?:задачу|задание)?\s*(один|два|три|четыре|пять|шесть|семь|восемь|девять|десять)/i,
      /(?:задачу|задание)?\s*(один|два|три|четыре|пять|шесть|семь|восемь|девять|десять)\s+(?:заверши|выполни)/i,

      // По названию задачи (без числа)
      /(?:заверши|отметь|выполни|сделай)\s+(?:задачу|задание)?\s*["']?(.+?)["']?$/i,
      /(?:отметь\s+)?["']?(.+?)["']?\s+(?:как\s+)?(?:выполненную|сделанную)/i,

      // Естественные формулировки
      /я\s+(?:сделал|выполнил|завершил)\s+(.+)/i,
      /сделано\s+(.+)/i,
      /(?:готово|готов|готова|готовы)(?:\s+(.+))?$/i
    ],
    delete: [
      // Прямые команды удаления (с числом)
      /(?:удали|удалить|сотри)\s+(?:задачу\s+)?(\d+)/i,
      /(?:задачу\s+)?(\d+)\s+(?:удали|сотри)/i,

      // Удаление по текстовому числу (один, два, три...)
      /(?:удали|удалить|сотри)\s+(?:задачу|задание)?\s*(один|два|три|четыре|пять|шесть|семь|восемь|девять|десять)/i,
      /(?:задачу|задание)?\s*(один|два|три|четыре|пять|шесть|семь|восемь|девять|десять)\s+(?:удали|сотри)/i,

      // Удаление по названию задачи (без числа)
      /(?:удали|удалить|сотри|убери)\s+(?:задачу|задание)?\s*["']?(.+?)["']?$/i,
      /(?:убери|удали)\s+(.+?)\s+(?:задачу|задание)/i,

      // Естественные формулировки
      /избавься\s+от\s+(?:задачи\s+)?(\d+)/i,
      /убери\s+(?:задачу\s+)?(\d+)/i
    ]
  },
  es: {
    add: [
      // Comandos directos de añadir
      /(?:añadir|crear|nueva)\s+(?:tarea\s+)?(.+)/i,
      /(?:añadir|crear|nueva)\s+(.+?)\s+(?:tarea|todo)/i,

      // Formulaciones naturales
      /necesito\s+(.+)/i,
      /tengo\s+que\s+(.+)/i,
      /recuérdame\s+(.+)/i,
      /no\s+olvides\s+(.+)/i,

      // Formas interrogativas
      /puedes\s+(?:añadir|crear)\s+(?:tarea\s+)?(.+)/i,
      /podrías\s+(?:añadir|crear)\s+(?:tarea\s+)?(.+)/i,

      // Fallback: cualquier texto que parezca una tarea
      /^(?!.*\b(?:muestra|ir|navega|cambia|elimina|completa|marca|termina)\b)(.+)$/i
    ],
    toggle: [
      // Comandos directos de completar
      /(?:completa|termina|marca|hecho)\s+(?:tarea\s+)?(\d+)/i,
      /(?:tarea\s+)?(\d+)\s+(?:completa|termina|hecho)/i,

      // Por nombre de tarea
      /(?:completa|termina|marca)\s+(?:tarea\s+)?["']?(.+?)["']?$/i,
      /(?:marca\s+)?["']?(.+?)["']?\s+(?:como\s+)?(?:completa|hecha)/i,

      // Formulaciones naturales
      /hice\s+(.+)/i,
      /terminé\s+con\s+(.+)/i
    ],
    delete: [
      // Comandos directos de eliminar
      /(?:elimina|borra|quita)\s+(?:tarea\s+)?(\d+)/i,
      /(?:tarea\s+)?(\d+)\s+(?:elimina|borra)/i,

      // Formulaciones naturales
      /deshazte\s+de\s+(?:tarea\s+)?(\d+)/i,
      /quita\s+(?:tarea\s+)?(\d+)/i
    ]
  }
};

// Весовые коэффициенты для разных паттернов (для будущей эвристики)
export const patternWeights = {
  explicit: 1.0,      // Явные команды ("добавь задачу")
  natural: 0.9,       // Естественные формулировки ("мне надо")
  question: 0.85,     // Вопросительные формы
  fallback: 0.7       // Fallback паттерны
};